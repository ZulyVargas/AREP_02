package edu.escuelaing.arem;

/**
 * @author Zuly Vargas
 */
public class Calculator {
    /**
     * Returns the cos value of the number.
     * @param number number.
     * @return cos value.
     */
    public static Double getCos(Double number ){
        return Math.cos(number);
    }
    /**
     * Returns the exp value of the number.
     * @param number number.
     * @return exp value.
     */
    public static Double getExp(Double number ){
        return Math.exp(number);
    }

}
